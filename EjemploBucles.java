package sentenciadecontrol;

public class EjemploBucles {
	public static void main(String[] args) {
		for (int aumentado = 0; aumentado < 25; aumentado++) {
			System.out.println( aumentado );
		}
		

		for (int disminuido = 100; disminuido > 0; disminuido--) {
			System.out.println( disminuido );
		}
	}	
}



