package Practica;

public class CuadradoAsteriscos {
	public static void main(String[] args) {
		int  A = 4;
		String B = "*" ;
		for ( B = "*" ; A < 4 ; A ++);
		System.out.print(B + " " + B + " " + B + " " + B);
		
		for ( B = "*" ; A < 4 ; A ++);
		System.out.print ('\n' + B +  "     "  +  B + '\n' + B +  "     "  +  B + '\n' + B );

		for (B = "*" ; A < 4 ; A ++);
		System.out.print(" "+ B + " " + B + " " + B );
		
}
}
